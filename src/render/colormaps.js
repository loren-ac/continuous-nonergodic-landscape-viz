function buildLUT(data) {
  const lut = new Float32Array(256 * 3);
  for (let i = 0; i < Math.min(data.length, 256); i++) {
    lut[i * 3] = data[i][0] / 255;
    lut[i * 3 + 1] = data[i][1] / 255;
    lut[i * 3 + 2] = data[i][2] / 255;
  }
  // If data is shorter than 256, extend with last color
  for (let i = data.length; i < 256; i++) {
    lut[i * 3] = lut[(data.length - 1) * 3];
    lut[i * 3 + 1] = lut[(data.length - 1) * 3 + 1];
    lut[i * 3 + 2] = lut[(data.length - 1) * 3 + 2];
  }
  return lut;
}

