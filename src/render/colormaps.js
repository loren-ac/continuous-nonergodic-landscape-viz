// Map a normalized value [0,1] to RGB [0,1] using a LUT
export function sampleColormap(name, t) {
  const lut = colormaps[name];
  if (!lut) return [1, 1, 1];
  const idx = Math.max(0, Math.min(255, Math.round(t * 255)));
  return [lut[idx * 3], lut[idx * 3 + 1], lut[idx * 3 + 2]];
}

// Fill a Float32Array of colors (length N*3) from values (length N)
export function mapColors(colorName, values, vMin, vMax, outColors) {
  const lut = colormaps[colorName];
  if (!lut) return;
  const range = vMax - vMin || 1;
  const N = values.length;
  for (let i = 0; i < N; i++) {
    const t = (values[i] - vMin) / range;
    const idx = Math.max(0, Math.min(255, Math.round(t * 255)));
    outColors[i * 3] = lut[idx * 3];
    outColors[i * 3 + 1] = lut[idx * 3 + 1];
    outColors[i * 3 + 2] = lut[idx * 3 + 2];
  }
}

export function listColormaps() {
  return Object.keys(colormaps);
}
