// Interpolate control points to 256 entries
function interpolateCtrl(ctrl) {
  const data = [];
  for (let i = 0; i < 256; i++) {
    const t = i / 255 * (ctrl.length - 1);
    const lo = Math.floor(t);
    const hi = Math.min(lo + 1, ctrl.length - 1);
    const f = t - lo;
    data.push([
      Math.round(ctrl[lo][0] + (ctrl[hi][0] - ctrl[lo][0]) * f),
      Math.round(ctrl[lo][1] + (ctrl[hi][1] - ctrl[lo][1]) * f),
      Math.round(ctrl[lo][2] + (ctrl[hi][2] - ctrl[lo][2]) * f),
    ]);
  }
  return data;
}

// Procedural colormaps
function generateGrayscale() {
  const data = [];
  for (let i = 0; i < 256; i++) data.push([i, i, i]);
  return data;
}

function generateCoolwarm() {
  // Diverging: blue [59,76,192] -> white [221,221,221] -> red [180,4,38]
  const blue = [59, 76, 192];
  const white = [221, 221, 221];
  const red = [180, 4, 38];
  const data = [];
  for (let i = 0; i < 256; i++) {
    const t = i / 255;
    if (t < 0.5) {
      const f = t * 2;
      data.push([
        Math.round(blue[0] + (white[0] - blue[0]) * f),
        Math.round(blue[1] + (white[1] - blue[1]) * f),
        Math.round(blue[2] + (white[2] - blue[2]) * f),
      ]);
    } else {
      const f = (t - 0.5) * 2;
      data.push([
        Math.round(white[0] + (red[0] - white[0]) * f),
        Math.round(white[1] + (red[1] - white[1]) * f),
        Math.round(white[2] + (red[2] - white[2]) * f),
      ]);
    }
  }
  return data;
}

